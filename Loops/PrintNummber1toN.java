import java.util.*;
public class PrintNummber1toN{
public static void main (String args[]){

Scanner sc = new Scanner(System.in);

System.out.println("Give the value for N:");
int N = sc.nextInt();
int counter = 1;

while(counter <= N) {
    System.out.print(counter + " ");
    counter++;
}
System.out.println();































}
}